STEP_HTML = """\
<div class="step {step_css}">
  <span class="tag {tag_css}">{label}</span>
  <span class="timestamp">{timestamp}</span>
  <p>{description}</p>
</div>
"""


class AuditTrail:
    """IRIS 작업 감사 추적 기록기."""

    def __init__(self, task_summary: str, user: str = ""):
        self.task_summary = task_summary
        self.user = user
        self.start_time = datetime.now()
        self.end_time = None
        self.steps: list[dict] = []
        self.result_status = "진행 중"
        self.result_detail = ""
        self.success = True

    def add_step(self, step_type: str, description: str, timestamp: str | None = None):
        """
        감사 추적에 단계를 추가한다.

        Args:
            step_type: 단계 유형. "nav", "search", "read", "input", "submit",
                       "error", "resolve", "info" 중 하나.
            description: 수행한 동작에 대한 설명.
            timestamp: HH:MM:SS 형식의 시각. None이면 현재 시각 사용.
        """
        if step_type not in STEP_TYPES:
            step_type = "info"
        if timestamp is None:
            timestamp = datetime.now().strftime("%H:%M:%S")
        self.steps.append({
            "type": step_type,
            "description": description,
            "timestamp": timestamp,
        })

    def set_result(self, status: str, detail: str, success: bool = True):
        """
        최종 결과를 설정한다.

        Args:
            status: "성공", "실패", "부분 완료" 등.
            detail: 결과 상세 내용 (HTML 가능).
            success: 성공 여부.
        """
        self.end_time = datetime.now()
        self.result_status = status
        self.result_detail = detail
        self.success = success

    def render(self) -> str:
        """감사 추적 HTML을 생성하여 반환한다."""
        if self.end_time is None:
            self.end_time = datetime.now()

        # 단계 HTML 생성
        steps_parts = []
        for step in self.steps:
            info = STEP_TYPES.get(step["type"], STEP_TYPES["info"])
            steps_parts.append(STEP_HTML.format(
                step_css=info["css"],
                tag_css=info["tag_css"],
                label=info["label"],
                timestamp=step["timestamp"],
                description=_escape(step["description"]),
            ))
        steps_html = "\n".join(steps_parts) if steps_parts else "<p>기록된 단계 없음</p>"

        time_range = f"{self.start_time.strftime('%Y-%m-%d %H:%M:%S')} ~ {self.end_time.strftime('%H:%M:%S')}"

        return HTML_TEMPLATE.format(
            task_summary=_escape(self.task_summary),
            time_range=time_range,
            user=_escape(self.user) or "(미확인)",
            result_status=_escape(self.result_status),
            steps_html=steps_html,
            result_class="" if self.success else "result-fail",
            result_detail=self.result_detail,  # 이미 HTML일 수 있으므로 escape하지 않음
            generated_at=self.end_time.strftime("%Y-%m-%d %H:%M:%S"),
        )

    def save(self, path: str) -> str:
        """HTML 파일을 저장하고 경로를 반환한다."""
        html = self.render()
        p = Path(path)
        p.parent.mkdir(parents=True, exist_ok=True)
        p.write_text(html, encoding="utf-8")
        return str(p)

    def auto_save(self, output_dir: str) -> str:
        """타임스탬프 기반 파일명으로 자동 저장한다."""
        ts = datetime.now().strftime("%Y%m%d-%H%M%S")
        filename = f"iris-audit-{ts}.html"
        return self.save(str(Path(output_dir) / filename))


def _escape(text: str) -> str:
    """기본 HTML 이스케이프."""
    return (text
            .replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;"))


def main():
    parser = argparse.ArgumentParser(description="IRIS 감사 추적 HTML 생성")
    parser.add_argument("--json", required=True, help="감사 추적 단계 JSON 파일 경로")
    parser.add_argument("--output", required=True, help="출력 HTML 파일 경로")
    args = parser.parse_args()

    with open(args.json, "r", encoding="utf-8") as f:
        data = json.load(f)

    trail = AuditTrail(
        task_summary=data.get("task_summary", ""),
        user=data.get("user", ""),
    )

    for step in data.get("steps", []):
        trail.add_step(
            step_type=step.get("type", "info"),
            description=step.get("description", ""),
            timestamp=step.get("timestamp"),
        )

    result = data.get("result", {})
    trail.set_result(
        status=result.get("status", "완료"),
        detail=result.get("detail", ""),
        success=result.get("success", True),
    )

    out_path = trail.save(args.output)
    print(f"감사 추적 HTML 생성 완료: {out_path}")


if __name__ == "__main__":
    main()
