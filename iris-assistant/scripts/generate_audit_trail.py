#!/usr/bin/env python3
"""
IRIS 작업 감사 추적(Audit Trail) HTML 생성기.

사용법:
    python generate_audit_trail.py --output /path/to/output.html --json /path/to/steps.json

또는 Python에서 직접 호출:
    from generate_audit_trail import AuditTrail
    trail = AuditTrail(task_summary="연구비 지급현황 조회", user="홍길동")
    trail.add_step("nav", "워크라운지 > 마이R&D에서 과제수행 > 연구비 > 연구비 지급현황으로 이동")
    trail.add_step("search", "과제번호 RS-2024-00123456로 검색, 결과 1건")
    trail.add_step("read", "연구비 지급현황: 정부출연금 50,000,000원, 집행액 32,000,000원")
    html = trail.render()
    trail.save("/path/to/output.html")
"""

import json
import sys
import argparse
from datetime import datetime
from pathlib import Path

# 단계 유형별 CSS 클래스와 한국어 태그 이름
STEP_TYPES = {
    "nav":     {"css": "step-nav",     "tag_css": "tag-nav",     "label": "네비게이션"},
    "search":  {"css": "step-search",  "tag_css": "tag-search",  "label": "검색"},
    "read":    {"css": "step-read",    "tag_css": "tag-read",    "label": "데이터 조회"},
    "input":   {"css": "step-input",   "tag_css": "tag-input",   "label": "입력"},
    "submit":  {"css": "step-submit",  "tag_css": "tag-submit",  "label": "제출"},
    "error":   {"css": "step-error",   "tag_css": "tag-error",   "label": "오류"},
    "resolve": {"css": "step-resolve", "tag_css": "tag-resolve", "label": "해결"},
    "info":    {"css": "step-info",    "tag_css": "tag-info",    "label": "참고"},
}

